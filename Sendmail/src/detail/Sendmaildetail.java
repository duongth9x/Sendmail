package detail;

public class Sendmaildetail {
	private String yourmail;
	private String pass;
	private String sendtomail;
	private String subject;
	private String description;

	public String getYourmail() {
		return yourmail;
	}

	public void setYourmail(String yourmail) {
		this.yourmail = yourmail;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}
	
	public String getSendtomail() {
		return sendtomail;
	}

	public void setSendtomail(String sendtomail) {
		this.sendtomail = sendtomail;
	}
	
	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
